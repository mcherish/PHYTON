def add(a,b):
    print(f"Adding {a} + {b}")
    return a + b

def subtract(a,b):
    print(f"Subtract {a} - {b}")
    return a - b

def multiply(a,b):
    print(f"Multiply {a} * {b}")
    return a * b

def divice(a,b):
    print(f"Dividing {a} / {b}")
    return a / b

a = add(1,2)

b = subtract(3,4)

c = multiply(5,6)

d = float(divice(7,8))

print(f"{a},{b},{c},{d}")